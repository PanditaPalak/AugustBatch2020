package constructorsDemo;
/*Constructor is used to perform object initialization
 * The piece of code which we want to get executed at the time 
 * of object creation,can be done using constructors.
 * 
 * constructors are called as special methods
 * 
 * structure  of a constructor:
 * 
 * Rules:
 *  1. Constructor name must be same as that of the class.
 *  2. A constructor must have no return Type
 *  3. A constructor  cannot be abstract,static,final,synchronized.
 *  
 *   modifier  constructorName()
 *   {
 *      body of constructor;
 *   
 *   }
 *   
 *   public Test()
 *   {
 *      body of constructor  
 *   }
 * 
 * */
public class ConstructorDemo1 {

	public ConstructorDemo1()
	{
		System.out.println("hii");
	}
	
	
}
