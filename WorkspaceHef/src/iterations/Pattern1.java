package iterations;

public class Pattern1 {
     
	public static void main(String[] args)
	{
        int   row  ;
        
        for(row =1 ;row<=4;row++)
        {
        	for(int col = 1;col<=4;col++)
        	{
        		System.out.print(col);
        	}
        	
        	System.out.println();
        }
		
	}
}
